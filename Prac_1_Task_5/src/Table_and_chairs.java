public class Table_and_chairs {

    public static void main(String[] args) {

        int i, e;
        for (i = 2; i >= 1; i--) {
            System.out.print("*");
            for (e = 20; e >= 1; e--) {
                System.out.print(" ");
            }
            System.out.print("*");
            System.out.println("");

        }
        System.out.print("*");
        int a, b;
        for (a = 5; a >= 1; a--) {
            System.out.print(" ");
        }
        for (b = 10; b >= 1; b--) {
            System.out.print("*");
        }
        for (a = 5; a >= 1; a--) {
            System.out.print(" ");
        }
        System.out.print("*");
    }

}




