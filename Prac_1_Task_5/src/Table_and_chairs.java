public class Table_and_chairs {

    public static void main(String[] args) {

        int i, e;
        for (i = 2; i >= 1; i--) {
            System.out.print("*");
            for (e = 20; e >= 1; e--) {
                System.out.print(" ");
            }
            System.out.print("*");
            System.out.println("");

        }
    }
}




